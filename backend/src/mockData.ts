// Simple seeded deterministic pick
function pick<T>(arr: T[], i: number, offset = 0): T {
  return arr[(i * 7 + offset * 13 + 3) % arr.length];
}

function genRecords(): TableRecord[] {
  return NAMES.map((name, i) => {
    const idx = i + 1;
    const id = `rec_${String(idx).padStart(3, "0")}`;

    const priority = pick(PRIORITIES, i, 2);
    const source = pick(SOURCES, i, 5);
    const remark = pick(REMARKS, i, 8);

    const assigneeIdx = (i * 3 + 1) % USERS.length;
    const assignee = USERS[assigneeIdx].id;

    // Dates
    const createdDaysAgo = 180 - Math.floor((i * 179) / 99); // spread 180..1
    const createdAt = daysAgo(createdDaysAgo);
    const updatedAt = daysAgo(Math.max(1, createdDaysAgo - 3 - (i % 10)));
    const deadlineFutureDays = -1 * (5 + (i * 7) % 90); // -5 to -94 (future)

    // 工作量 PD 预估: integer 1-30
    const pdEstimate = 1 + ((i * 13 + 3) % 30);

    return {
      id,
      tableId: "tbl_requirements",
      cells: {
        fld_name: name,
        fld_created: createdAt,
        fld_assignee: assignee,
        fld_desc: DESCS[i],
        fld_priority: priority,
        fld_deadline: daysAgo(deadlineFutureDays),
        fld_source: source,
        fld_remark: remark,
        fld_pd_estimate: pdEstimate,
        fld_req_doc: pick(REQ_DOCS, i, 1),
        fld_tech_plan: pick(TECH_PLANS, i, 2),
        fld_test_plan: pick(TEST_PLANS, i, 3),
        fld_test_case: pick(TEST_CASES, i, 4),
      },
      createdAt,
      updatedAt,
    };
  });
}

// ─── Second table: 研发工时日志 ───
// Each record is a log entry for a requirement. fld_req_name links to tbl_requirements.fld_name by value.
// This lets us demo cross-table Lookups: "sum hours for this requirement", "count entries", etc.

const HISTORY_STATUS_OPTIONS = [
  { id: "opt_status_doing", name: "进行中", color: "#002270" },
  { id: "opt_status_done",  name: "已完成", color: "#02312A" },
  { id: "opt_status_paused", name: "已暂停", color: "#3B1A02" },
];

function genHistoryRecords(): TableRecord[] {
  const out: TableRecord[] = [];
  // For each requirement, seed 1-4 log entries
  NAMES.forEach((name, i) => {
    const entries = 1 + ((i * 7 + 3) % 4); // 1..4
    for (let k = 0; k < entries; k++) {
      const idx = out.length + 1;
      const id = `hist_${String(idx).padStart(4, "0")}`;
      const hours = 1 + ((i * 3 + k * 5 + 2) % 8); // 1..8
      const statusIdx = (i + k) % HISTORY_STATUS_OPTIONS.length;
      const status = HISTORY_STATUS_OPTIONS[statusIdx].name;
      const logDaysAgo = Math.max(1, 90 - ((i * 5 + k * 17) % 90));
      const logDate = daysAgo(logDaysAgo);
      const loggerIdx = (i * 2 + k + 1) % USERS.length;
      out.push({
        id,
        tableId: "tbl_history",
        cells: {
          hist_req_name: name,
          hist_hours: hours,
          hist_status: status,
          hist_log_date: logDate,
          hist_logger: USERS[loggerIdx].id,
        },
        createdAt: logDate,
        updatedAt: logDate,
      });
    }
  });
  return out;
}

